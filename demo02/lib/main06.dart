//비교연산자
//두개의 값을 비교하여 결과를 참/거짓을 판별할 때 사용한다.
void main() {
  //같다
  print('2 == 3 => ${2 == 3}'); // false

  //다르다
  print('2 != 3 => ${2 != 3}'); // true

  //왼쪽값이 오른쪽값보다 작다
  print('2 < 3 => ${2 < 3}'); // ture

  //왼쪽값이 오른쪽 값보다 크다
  print('2 > 3 => ${2 > 3}'); // false
  //왼쪽값이 오른쪽 값보다 작거나 같다.
  print('2 <= 3 => ${2 <= 3}'); // ture

  // 왼쪽값이 오른쪽 값보다 크거나 같다.
  print('2 >= 3 => ${2 >= 3}'); // false
}
